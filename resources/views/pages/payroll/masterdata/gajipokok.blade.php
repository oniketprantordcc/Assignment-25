{{-- Script Open Modal Callback --}}
<script>
    $(document).ready(function () {
        $('#validasierror').click();

        $('#besaran_gaji').on('input', function () {
            var nominal = $(this).val()
            var nominal_fix = new Intl.NumberFormat('id', {
                style: 'currency',
                currency: 'IDR'
            }).format(nominal)

            $('#detailbesarangaji').html(nominal_fix);
        })

        $('.edit_gaji').each(function () {
            $(this).on('input', function () {
                var harga = $(this).val()
                var harga_fix = new Intl.NumberFormat('id', {
                    style: 'currency',
                    currency: 'IDR'
                }).format(harga)
                var harga_paling_fix = $(this).parent().find('.detailgajiedit')
                $(harga_paling_fix).html(harga_fix);
            })
        })
    });

</script>

